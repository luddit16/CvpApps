package com.cvpapps.model;

public class CVPAdmin {

	private String adminId;
	private String adminUserName;
	private String adminPassword;
	private String adminFirstName;
	private String adminLastname;
	private String adminEmail;
	private String adminMobile;
	private String adminZone;
	private String adminImage;
	
	
	
	
	public String getAdminId() {
		return adminId;
	}
	public void setAdminId(String adminId) {
		this.adminId = adminId;
	}
	public String getAdminUserName() {
		return adminUserName;
	}
	public void setAdminUserName(String adminUserName) {
		this.adminUserName = adminUserName;
	}
	public String getAdminPassword() {
		return adminPassword;
	}
	public void setAdminPassword(String adminPassword) {
		this.adminPassword = adminPassword;
	}
	public String getAdminFirstName() {
		return adminFirstName;
	}
	public void setAdminFirstName(String adminFirstName) {
		this.adminFirstName = adminFirstName;
	}
	public String getAdminLastname() {
		return adminLastname;
	}
	public void setAdminLastname(String adminLastname) {
		this.adminLastname = adminLastname;
	}
	public String getAdminEmail() {
		return adminEmail;
	}
	public void setAdminEmail(String adminEmail) {
		this.adminEmail = adminEmail;
	}
	public String getAdminMobile() {
		return adminMobile;
	}
	public void setAdminMobile(String adminMobile) {
		this.adminMobile = adminMobile;
	}
	public String getAdminZone() {
		return adminZone;
	}
	public void setAdminZone(String adminZone) {
		this.adminZone = adminZone;
	}
	public String getAdminImage() {
		return adminImage;
	}
	public void setAdminImage(String adminImage) {
		this.adminImage = adminImage;
	}
	
	
	
}
