package com.cvpapps.model;

public class CVPSuperAdmin {
	
	
	private String sAdminId;
	private String sAdminUsername;
	private String sAdminPassword;
	private String sAdminFirstName;
	private String sAdminLastName;
	private String sAdminEmail;
	private String sAdminMobile;
	private String sAdminImage;
	public String getsAdminId() {
		return sAdminId;
	}
	public void setsAdminId(String sAdminId) {
		this.sAdminId = sAdminId;
	}
	public String getsAdminUsername() {
		return sAdminUsername;
	}
	public void setsAdminUsername(String sAdminUsername) {
		this.sAdminUsername = sAdminUsername;
	}
	public String getsAdminPassword() {
		return sAdminPassword;
	}
	public void setsAdminPassword(String sAdminPassword) {
		this.sAdminPassword = sAdminPassword;
	}
	public String getsAdminFirstName() {
		return sAdminFirstName;
	}
	public void setsAdminFirstName(String sAdminFirstName) {
		this.sAdminFirstName = sAdminFirstName;
	}
	public String getsAdminLastName() {
		return sAdminLastName;
	}
	public void setsAdminLastName(String sAdminLastName) {
		this.sAdminLastName = sAdminLastName;
	}
	public String getsAdminEmail() {
		return sAdminEmail;
	}
	public void setsAdminEmail(String sAdminEmail) {
		this.sAdminEmail = sAdminEmail;
	}
	public String getsAdminMobile() {
		return sAdminMobile;
	}
	public void setsAdminMobile(String sAdminMobile) {
		this.sAdminMobile = sAdminMobile;
	}
	public String getsAdminImage() {
		return sAdminImage;
	}
	public void setsAdminImage(String sAdminImage) {
		this.sAdminImage = sAdminImage;
	}
	
	
	

}
